public class MessageApp {

    // public:全てのクラスからアクセス可能
    // static:インスタンス化せずに使用可能
    // void:戻り値なし
    // main:プログラム本文
    // String[] args:コマンド引数
    public static void main(String[] args) {
        // println:出力の終わりに改行（sysoで予測出てくる）
        System.out.println("こんにちは！");
        // print:出力の終わりに改行しない
        System.out.print("私の名前は");
        System.out.print("佐藤です");
    }
}
